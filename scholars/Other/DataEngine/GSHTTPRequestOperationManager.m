#import "GSHTTPRequestOperationManager.h"
#import "GSTaskEngine.h"
#import "GSHTTPURLRequestSerializer.h"
@implementation GSHTTPRequestOperationManager
- (instancetype)initWithBaseURL:(NSURL *)url
{
    self = [super initWithBaseURL:url];
    
    if (self)
    {
        self.operationQueue = [GSTaskEngine sharedEngine].httpRequestOperationQueue;
        self.requestSerializer = [GSHTTPURLRequestSerializer serializer];
    }
    return self;
}
@end
